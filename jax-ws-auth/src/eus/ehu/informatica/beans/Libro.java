package eus.ehu.informatica.beans;

public class Libro {

	private String isbn;
	private String titulo;
	
	
	public Libro() {
		super();
		
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	
	
}
